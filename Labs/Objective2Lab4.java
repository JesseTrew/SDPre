public class Objective2Lab4 {
    public static void main(String[] args) {
        double num1, num2, num3, sum, average;

        num1 = 2.5;
        num2 = 5.2;
        num3 = 9.9;
        sum = num1 + num2 + num3;
        average = sum / 3;
        System.out.println( "The average of " + num1 + " and " + num2 + " and " + num3 + " is " + average + ".");
    }
}
